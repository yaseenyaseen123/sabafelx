// Add counter animation for features
function animateCounters() {
    const counters = document.querySelectorAll('.feature-item span');
    
    counters.forEach(counter => {
        const text = counter.textContent;
        const numbers = text.match(/\d+/g);
        
        if (numbers) {
            const finalNumber = parseInt(numbers[0]);
            let currentNumber = 0;
            const increment = finalNumber / 100;
            
            const timer = setInterval(() => {
                currentNumber += increment;
                if (currentNumber >= finalNumber) {
                    currentNumber = finalNumber;
                    clearInterval(timer);
                }
                
                counter.textContent = text.replace(/\d+/, Math.floor(currentNumber).toLocaleString());
            }, 20);
        }
    });
}

// Initialize counter animation when servers section is visible
const serversSection = document.querySelector('.servers');
if (serversSection) {
    const serversObserver = new IntersectionObserver(function(entries) {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                animateCounters();
                serversObserver.unobserve(entry.target);
            }
        });
    }, { threshold: 0.5 });
    
    serversObserver.observe(serversSection);
}

